    <h1>Medical Treatments for psoriasis</h1>
    <h2>Medical treatments</h2>

    <p>Firstly there is no cure for <strong>psoriasis</strong>. It can disappear for many years and sometimes never return and other times return with less or more severity.</p>

    <h2>Doctors can prescribe steriods</h2>
    <p>Topical steroids-derived from the natural corticosteroid hormones produced by the adrenal glands-are the most frequently used treatment for <strong>psoriasis</strong>. Corticosteroids have many important functions in the body, including control of inflammatory responses. Topical steroids are used as anti-inflammatory agents to reduce the swelling and redness of lesions.</p>

    <p>Steroids come in various strengths, ranging relatively mild to extremely potent. Steriods can bring a effective temporary relief from <strong>psoriasis</strong>. However steroids should be used sparingly on the affected areas only.</p>

    <h2>What are the risks / side effects?</h2>
    <li>Do not use a topical steroid for longer than three weeks without consulting your doctor.</li>
    <li>Refrain from abruptly discontinuing a topical steroid as it can cause your <strong>psoriasis</strong> to flare.</li>
    <li>Unless the medication is formulated for the eye area, do not use steroids on, in, or around the eyes, as cataracts and glaucoma can result.</li>
    <li>The more potent the steroid, the more effective it is in clearing <strong>psoriasis</strong>, but the risk of side effects is greater.</li>
    <li>Low-strength steroids are good for treating the face, groin and breasts, but care must be taken as the risk of side effects is greater in sensitive skin areas.</li>
    <li>Potential side effects of topical steroids include skin damage, such as skin thinning, changes in pigmentation, easy bruising, stretch marks, redness and dilated surface blood vessels. Steroids can be absorbed through the skin and affect internal organs when applied to widespread areas of skin, used over long periods of time, or used with excessive occlusion.</li>  

    <h2>Doctors can also prescribe UV treatment </h2>

    <h2>Why ultraviolet light may help</h2>
    <p>The use of the sun's rays has been used to treat <strong>psoriasis</strong> for over a century and 80% of people notice an improvement in their skin after exposure to sunlight.</p> 

    <p>UV light helps to power chemical reactions that affect the function of skin cells. In <strong>psoriasis</strong>, this means that the skin cells do not multiply so rapidly, and behave more like normal skin.</p>

    <h2>What are the risks / side effects?</h2>
    <li>Exposure to UV light (A or B) can cause skin damage, premature ageing and increases the risk of skin cancer. For these reasons, the British Photodermatology Group have issued guidelines as to the total number of UV treatments an individual can have in a lifetime.</li>
    <li>Some redness of the skin and subsequent tanning is likely, but you should let the staff in the department know if you experience any burning sensations (usually 12-24 hours after treatment).</li>
    <li>UV treatment can cause the skin to become dry and subsequently itchy - apply plenty of moisturiser in order to overcome this. However, if the itching worsens, do tell the phototherapy nurse or dermatologist.</li>
